"""Deterministic generator of the dummy_dose_v0 ladder fixtures (rungs 2-6).

Factory-side tooling (bootstrap exception, Decision Log v0.10: hand-built
ladder is Slice-1-only and expires with automatic rival derivation).

Everything is derived from a FIXED observational pool (POOL_SEED) drawn from
the true world: re-running this script must reproduce the committed fixtures
byte-for-byte. Fits are closed-form OLS / moments - no optimizer randomness.

It also calibrates the provisional lambda (Decision Log v0.11): lambda such
that the MDL term of world.py equals 5% of the FIDELITY range
(fid_truth - fid_null) on the production battery, and writes it to meta.json.

Run:  .venv/Scripts/python cases/dummy_dose_v0/make_ladder_fixtures.py
"""

import json
from pathlib import Path
from types import SimpleNamespace

import numpy as np

CASE_DIR = Path(__file__).parent
POOL_SEED = 20260611
POOL_N = 20000
PERTURB = 1.15  # fixed multiplier on the three structural coefficients (rung 2)


def observational_pool():
    from wager.factory.case_loader import load_world_sample

    sample = load_world_sample(CASE_DIR)
    regime = SimpleNamespace(config={}, context={"cohort": 0.0}, horizon=None)
    return sample(regime, POOL_N, POOL_SEED)


def saturating(dose):
    return 10.0 * dose / (dose + 4.0)


def fit_numbers(pool):
    dose = pool["dose"].to_numpy()
    marker = pool["marker"].to_numpy()
    outcome = pool["outcome"].to_numpy()

    # rung 3: straight line fitted to sat(d) over the historical dose support
    sat_slope, sat_intercept = np.polyfit(dose, saturating(dose), 1)

    # rung 4: OLS outcome ~ sat(dose) on the corrupted pool (the naive causal read)
    x = np.column_stack([np.ones_like(dose), saturating(dose)])
    beta, *_ = np.linalg.lstsq(x, outcome, rcond=None)
    resid_sd = float(np.std(outcome - x @ beta))

    # rung 5: joint gaussian moments of the pool
    data = np.column_stack([dose, marker, outcome])
    mu = data.mean(axis=0)
    cov = np.cov(data, rowvar=False)

    return {
        "sat_slope": float(sat_slope),
        "sat_intercept": float(sat_intercept),
        "beta0": float(beta[0]),
        "beta1": float(beta[1]),
        "resid_sd": resid_sd,
        "mu_dose": float(dose.mean()),
        "sd_dose": float(dose.std()),
        "mu_marker": float(marker.mean()),
        "sd_marker": float(marker.std()),
        "mu_outcome": float(outcome.mean()),
        "sd_outcome": float(outcome.std()),
        "mu": [float(v) for v in mu],
        "cov": [[float(v) for v in row] for row in cov],
    }


HEADER = (
    "GENERATED by make_ladder_fixtures.py (POOL_SEED={seed}, POOL_N={n}) - do not\n"
    "hand-edit; regenerate to audit. If an ordering fails, the default is to\n"
    "INVESTIGATE, never to retune (Decision Log v0.11)."
)


def rung_2(f):
    header = HEADER.format(seed=POOL_SEED, n=POOL_N)
    return f'''"""Rung 2 - truth with the three structural coefficients perturbed x{PERTURB}.

RATIONALE (rung 1 -> 2): a fixed 15% proportional error on assignment strength,
dose effect and severity effect produces a small but systematic mismatch in
EVERY regime - in-support, off-support and observational alike. Expected
strictly below the exact truth (whose only mismatch is sampling noise) and
strictly above rungs that break structure rather than calibration.

{header}
"""
import numpy as np
import pandas as pd

SEV_COEF_DOSE = {1.5 * PERTURB!r}
DOSE_BASE = 2.0
DOSE_NOISE = 1.0
EFFECT_DOSE = {1.0 * PERTURB!r}
EFFECT_SEV = {-2.0 * PERTURB!r}
OUTCOME_NOISE = 0.5
MARKER_NOISE = 1.5


def _saturating(dose):
    return 10.0 * dose / (dose + 4.0)


def model(regime, n, seed):
    rng = np.random.default_rng(seed)
    cohort = regime.context.get("cohort", 0.0)
    severity = rng.normal(cohort, 1.0, n)
    if "dose" in regime.config:
        dose = np.full(n, float(regime.config["dose"]))
    else:
        raw = DOSE_BASE + SEV_COEF_DOSE * severity + rng.normal(0.0, DOSE_NOISE, n)
        dose = np.clip(raw, 0.0, 10.0)
    outcome = (
        EFFECT_DOSE * _saturating(dose)
        + EFFECT_SEV * severity
        + rng.normal(0.0, OUTCOME_NOISE, n)
    )
    marker = severity + rng.normal(0.0, MARKER_NOISE, n)
    return pd.DataFrame({{"dose": dose, "marker": marker, "outcome": outcome}})
'''


