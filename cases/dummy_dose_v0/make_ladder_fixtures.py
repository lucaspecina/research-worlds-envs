def write_fixtures(numbers) -> None:
    ladder_dir = CASE_DIR / "ladder"
    ladder_dir.mkdir(exist_ok=True)
    fixtures = {
        "rung_2_perturbed.py": rung_2(numbers),
        "rung_3_linearized.py": rung_3(numbers),
        "rung_4_innocent_twin.py": rung_4(numbers),
        "rung_5_naive_fit.py": rung_5(numbers),
        "rung_6_null.py": rung_6(numbers),
    }
    for name, content in fixtures.items():
        (ladder_dir / name).write_text(content, encoding="utf-8", newline="\n")
        print(f"wrote ladder/{name}")


def calibrate_lambda() -> None:
    """lambda such that lambda * mdl(world.py) = 5% of the NORMALIZATION range
    (fid_truth - fid_naive), the range R maps to [0, 1] (Decision Log v0.12).
    The naive fit (rung 5) is the S_naive anchor; the null is a pathological
    off-support outlier and is NOT a good range basis."""
    from wager.contracts import ScoringParams
    from wager.factory.case_loader import (
        load_battery,
        load_ladder,
        load_meta,
        load_world_sample,
        load_world_source,
    )
    from wager.reward.mdl import mdl_bytes
    from wager.reward.scorer import WorldSide, sandboxed_null_sample, score_submission

    meta = load_meta(CASE_DIR)
    battery = load_battery(CASE_DIR)
    world_sample = load_world_sample(CASE_DIR)
    world_source = load_world_source(CASE_DIR)
    ladder = dict(load_ladder(CASE_DIR))
    naive_code = ladder["rung_5_naive_fit"]
    null_code = ladder["rung_6_null"]
    params = ScoringParams(
        lambda_mdl=0.0,
        n_samples=meta.scoring.n_samples,
        m_reps=meta.scoring.m_reps,
        model_call_timeout_s=meta.scoring.model_call_timeout_s,
    )
    with sandboxed_null_sample(null_code, meta.column_names, params.model_call_timeout_s) as null_sample:
        world_side = WorldSide(
            world_sample, battery, meta.column_names, params.n_samples, null_sample=null_sample
        )
        fid_truth = score_submission(world_source, world_side, params).fidelity
        fid_naive = score_submission(naive_code, world_side, params).fidelity
    fid_range = fid_truth - fid_naive
    lam = 0.05 * fid_range / mdl_bytes(world_source)

    meta_path = CASE_DIR / "meta.json"
    raw = json.loads(meta_path.read_text(encoding="utf-8"))
    raw["scoring"]["lambda_mdl"] = lam
    raw["scoring"]["lambda_provisional"] = True
    meta_path.write_text(json.dumps(raw, indent=2) + "\n", encoding="utf-8", newline="\n")
    print(f"fid_truth={fid_truth:.6f} fid_naive={fid_naive:.6f} norm_range={fid_range:.6f}")
    print(f"lambda_mdl={lam!r} (provisional, =5% of normalization range) -> meta.json")


if __name__ == "__main__":
    pool = observational_pool()
    numbers = fit_numbers(pool)
    write_fixtures(numbers)
    calibrate_lambda()
