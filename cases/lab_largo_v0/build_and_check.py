LEVELS_ABCD = [0.5, 1.8, 3.0, 4.2, 5.5, 6.8, 8.2, 9.5]
LEVELS_L = [0.5, 2.2, 4.2, 4.7, 5.3, 5.8, 7.6, 9.0]
N_SEEDS = 3

NS = lambda c: SimpleNamespace(config=dict(c), context={}, horizon=None)  # noqa: E731


def build_battery():
    items, seeds = [], iter(range(77001, 77001 + 64))
    for name in world.LINES:
        levels = LEVELS_L if name == 1 else LEVELS_ABCD
        for lvl in levels:
            items.append(BatteryItem(
                weight=0.025, seed_world=next(seeds),
                regime=Regime(config={"line": name, "driver": lvl}, context={})))
    return Battery(items=items)


def write_truth_code():
    phases = ", ".join(f"{v!r}" for v in world.PHASES)
    lp = repr(world.LINE_PARAMS)
    noise = world.PARAMS["y_noise"]
    code = f'''"""truth_code -- R=1 anchor of lab_largo_v0 (deliverable column only).
GENERATED by build_and_check.py - do not hand-edit."""
import numpy as np
import pandas as pd

AMPS = np.array([1.00, 0.72, 0.52, 0.38, 0.28, 0.20, 0.15, 0.11])
FREQS = np.array([0.35, 0.55, 0.80, 1.10, 1.50, 2.00, 2.70, 3.60])
PHASES = np.array([{phases}])
LP = {lp}


def _g(name, d):
    if int(name) == 1:
        base = 12.0 + 16.0 * d ** 3 / (27.0 + d ** 3) - 4.0 / (1.0 + np.exp(-(d - 8.0) / 0.45))
        win = np.exp(-np.abs((d - 5.0) / 1.35) ** 8.0)
        local = np.zeros_like(d)
        for a, f, ph in zip(AMPS, FREQS, PHASES):
            local += a * np.sin(2.0 * np.pi * f * (d - 3.5) + ph)
        return base + win * local
    q = LP[int(name)]
    return (q["mu"] + q["beta"] * np.tanh((d - q["c"]) / q["s"])
            + q["gamma"] * np.exp(-((d - q["h"]) ** 2) / (2.0 * q["w"] ** 2)))


def model(regime, n, seed):
    rng = np.random.default_rng(seed)
    d = np.full(n, float(regime.config["driver"]))
    y = _g(int(regime.config["line"]), d) + rng.normal(0.0, {noise!r}, n)
    return pd.DataFrame({{"outcome": y}})
'''
    (CASE / "truth_code.py").write_text(code, encoding="utf-8", newline="\n")


def free_data_canonical():
    """The pilot-naive's information set: free sources only, canonical seeds."""
    data = {}
    ov = world.sample(NS({}), 48, 61001)
    data[1] = (ov["driver"].to_numpy(float), ov["outcome"].to_numpy(float))
    for name in (2, 3, 4, 5):
        p = world.sample(NS({"__pilot": name}), 4, 61002)
        data[name] = (p["driver"].to_numpy(float), p["outcome"].to_numpy(float))
    return data


def write_fixtures():
    naive_code = robots.portfolio_submission(free_data_canonical())
    pool = np.concatenate([
        world.sample(NS({"line": nm, "driver": dv}), 400, 61010 + i)["outcome"].to_numpy(float)
        for i, (nm, dv) in enumerate((n, d) for n in world.LINES for d in (2.0, 5.0, 8.0))])
    null_code = (f'"""Rung 8 -- null: collapsed variance at the pooled mean."""\n'
                 f"import numpy as np\nimport pandas as pd\n\n\n"
                 f"def model(regime, n, seed):\n"
                 f"    rng = np.random.default_rng(seed)\n"
                 f"    return pd.DataFrame({{'outcome': rng.normal({float(pool.mean())!r}, 0.01, n)}})\n")
    ladder = CASE / "ladder"
    ladder.mkdir(exist_ok=True)
    (ladder / "rung_7_naive_pilots.py").write_text(
        '"""Rung 7 -- the free shippable (S_naive, R=0): overview + pilots only."""\n'
        + naive_code, encoding="utf-8", newline="\n")
    (ladder / "rung_8_null.py").write_text(null_code, encoding="utf-8", newline="\n")
    return pool


def pozo_line_search(meta, battery):
    """The pit's single campaign goes to the LINE that maximizes its own final
    score (best possible tail, 4 candidates)."""
    params = meta.scoring.model_copy(update={"n_samples": 800, "m_reps": 1})
    ws = WorldSide(world.sample, battery, ["outcome"], params.n_samples,
                   null_sample=None, functionals=meta.stakes.functionals,
                   c_f=params.c_f)
    base = {k: [np.array(v[0]), np.array(v[1])] for k, v in free_data_canonical().items()}
    for k in range(1, 9):
        df = world.sample(NS({"__archive_layer": k}), 96, 62000 + k)
        base[1] = [np.concatenate([base[1][0], df["driver"].to_numpy(float)]),
                   np.concatenate([base[1][1], df["outcome"].to_numpy(float)])]
    best, best_score = None, -np.inf
    for name in (2, 3, 4, 5):
        data = {k: (np.array(v[0]), np.array(v[1])) for k, v in base.items()}
        e = world.sample(NS({"campaign_line": name}), 20, 63000)
        data[name] = (np.concatenate([data[name][0], e["driver"].to_numpy(float)]),
                      np.concatenate([data[name][1], e["outcome"].to_numpy(float)]))
        env = {}
        exec(robots.portfolio_submission(data), env)
        s = score_callable(env["model"], ws, params)
        if s > best_score:
            best, best_score = name, s
    return best


def main():
    meta = CaseMeta.from_json_file(CASE / "meta.json")
    battery = build_battery()
    battery.to_json_file(CASE / "battery.json")
    write_truth_code()
    pool = write_fixtures()

    line = pozo_line_search(meta, battery)
    print(f"pozo best-tail line: {line}", flush=True)
    src = (CASE / "robots.py").read_text(encoding="utf-8")
    src = src.replace(
        next(ln for ln in src.splitlines() if ln.startswith("POZO_LINE")),
        f'POZO_LINE = {line}  # baked by build_and_check.py (best-tail search)')
    (CASE / "robots.py").write_text(src, encoding="utf-8", newline="\n")
    import importlib
    importlib.reload(robots)

    checks: dict = {"pozo_line": line}

    server = build_world_server(CASE, seed_offset=3)
    ok = server.submit((CASE / "truth_code.py").read_text(encoding="utf-8"))
    checks["contract_runs"] = bool(ok.accepted)
    checks["truth_R"] = round(server.result["R"], 4)

    hacks = {
        "constante_global": (f"import numpy as np\nimport pandas as pd\n\n"
                             f"def model(regime, n, seed):\n"
                             f"    rng = np.random.default_rng(seed)\n"
                             f"    return pd.DataFrame({{'outcome': rng.normal({float(pool.mean())!r}, {float(pool.std())!r}, n)}})\n"),
        "varianza_colapsada": (CASE / "ladder" / "rung_8_null.py").read_text(encoding="utf-8"),
        "ruido_x2": (CASE / "truth_code.py").read_text(encoding="utf-8").replace(
            f"{world.PARAMS['y_noise']!r}, n", f"{2 * world.PARAMS['y_noise']!r}, n"),
    }
    checks["hacks"] = {}
    for name, code in hacks.items():
        server = build_world_server(CASE, seed_offset=3)
        r = server.submit(code)
        checks["hacks"][name] = round(server.result["R"], 4) if r.accepted else None
        print(f"  hack {name}: R={checks['hacks'][name]}", flush=True)
    # blind hacks (no curve knowledge) must stay near the floor; a variance
    # defect ON the true curve must lose >= 0.05 (the v0 red-team standard for
    # material defects -- 0.85-for-everything was an arbitrary first guess).
    checks["hacks_low"] = (checks["hacks"]["constante_global"] <= 0.30
                           and checks["hacks"]["varianza_colapsada"] <= 0.30
                           and checks["hacks"]["ruido_x2"] <= 1.0 - 0.05)

    for name in ("juicio", "pozo"):
        rs = []
        for k in range(N_SEEDS):
            res = robots.run_robot(CASE, name, seed_offset=20 + k)
            rs.append(res["R"])
            print(f"  robot {name} seed {20 + k}: R={res['R']:.4f} (left {res['left']:.0f})",
                  flush=True)
        checks[name] = [round(v, 4) for v in rs]
    checks["juicio_reaches"] = min(checks["juicio"]) >= 0.90
    checks["pozo_pays"] = max(checks["pozo"]) <= 0.60
    checks["separation"] = round(min(j - p for j, p in zip(checks["juicio"], checks["pozo"])), 4)
    checks["separation_ok"] = checks["separation"] >= 0.30
    checks["all"] = all([checks["contract_runs"], checks["hacks_low"],
                         checks["juicio_reaches"], checks["pozo_pays"],
                         checks["separation_ok"]])

    print(json.dumps(checks, indent=2))
    (CASE / "check.json").write_text(json.dumps(checks, indent=2) + "\n", encoding="utf-8")
    print("-> battery.json + truth_code.py + ladder + check.json")


if __name__ == "__main__":
    main()
